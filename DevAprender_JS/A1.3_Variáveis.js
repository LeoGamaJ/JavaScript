// Tipos primitivos e tipagem

let nome = 'Leo';           // String literal
let idade = 25;             // Number Literal
let estaAprovado = true;    // Boolean
let sobrenome = undefined;  // Undefined
let corSelecionado = null;  // Redefinir um valor

// typeof()
console.log(nome)
console.log(typeof(nome))

console.log(idade)
console.log(typeof(idade))

console.log(estaAprovado)
console.log(typeof(estaAprovado))

console.log(sobrenome)
console.log(typeof(sobrenome))

console.log(corSelecionado)
console.log(typeof(corSelecionado))

console.log(`O nome ${nome} tem a tipagem: ${typeof(nome)}.`)
console.log(`A idade informada foi ${idade} anos. Esta tipagem é: ${typeof(idade)}.`)
console.log(`É ${estaAprovado} a situação do aprovado. Esta tipagem é: ${estaAprovado}. tem a tipagem: ${typeof(nome)}.`)
console.log(`O sobrenome ${sobrenome} tem a tipagem: ${typeof(sobrenome)}.`)
console.log(`O item cor selecionada se encontra: ${corSelecionado}. Esta tipagem é: ${typeof(corSelecionado)}.`)